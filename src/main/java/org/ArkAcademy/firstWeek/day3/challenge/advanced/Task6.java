package day3.challenge.advanced;

public class Task6 {
    public static void main(String[] args) {
        // Challenge 6: Odd or Even with For Loop
        for(int i = 1; i<= 10;i++){
            if( i % 2 == 0){
                System.out.println(i + "  is Even");
            }else{
                System.out.println(i + "  is Odd");
            }
        }
    }
}
